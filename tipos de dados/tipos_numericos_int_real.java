package geek;

// Tipos de dados
// Numéricos (Inteiros e Reais)
public class tipos_numericos_int_real {
	public static void main(String[] args) {
		// Tipos primários/primitivos
		int num1 = 4; // Inteiro 9999999999999
		short num2 = 4; // Inteiro (curto / menor / baixo) 99999999
		
		// Tipos não primários/primitivos
		Integer num3 = 98;
		Short num4 = 7;
		
		System.out.println("int -> Num1 = " + num1);
		System.out.println("short -> Num2 = " + num2);
		System.out.println("Integer -> Num3 = " + num3);
		System.out.println("Short -> Num4 = " + num4);
		
	}

}
